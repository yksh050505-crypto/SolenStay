String _two(int n) => n < 10 ? '0$n' : '$n';

/// 해당 날짜가 공휴일이면 이름, 아니면 null.
String? holidayName(DateTime day) =>
    _holidays['${day.year}-${_two(day.month)}-${_two(day.day)}'];

/// 해당 날짜가 공휴일인지.
bool isHoliday(DateTime day) => holidayName(day) != null;
